public class Tiles {
    private String color;

    public Tiles() {
        this.color = "Grey";
    }

    public Tiles(String color) {
        this.color = color;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getTileType() {
        return color;
    }
}
